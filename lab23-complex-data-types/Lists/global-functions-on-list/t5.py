# numbers in list as a string

t1 = [ 1, 5, 3, 4, 0 ]
t2 = [ "9", "2","3","0","5"]

print(f" t1 : {t1}")
print(f" t2 : {t2}")

print(f" Sorted t1 : {sorted(t1)}")   # str > str  : allowed , str < int : not allowed ,
print(f" Sorted t2 : {sorted(t2)}") #
# sorted() : it return new sorted list , as we can see original list below
print(f" t1 : {t1}")
print(f" t2 : {t2}")

# we can only perform sorted() function on similar type of data or homogenous type of data

