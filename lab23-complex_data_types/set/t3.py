# Declaration of set

s1 = { 1 , 3} # set is collection of distinct elements separated by comma within Curly brackets {}

print(f" set 1 : {s1}")
print(f" Type of set 1 : {s1}")

s2 = {} # is it a empty set ?
# let's check its type
print(f" set 2 is : {s2}")
print(f"Type of set 2 is : {type(s2)}")

s3 = set()
print(f" set 3 is : {s3}")
print(f"Type of set 3 is : {type(s3)}")

