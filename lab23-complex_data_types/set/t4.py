# Union in set
# union of set returns a new set contains all distinct elements from both sets
s1 = { 1 , 3, 4, 5, 5} # duplicate elements will remove
s2 = { 3 , 4 ,5 ,6}

print(f" set 1 : {s1}")
print(f" set 2 : {s2}")

# union of set1 and set2 , uni

print(s1 | s2) # symbol for union of set in python is '|' pipe symbol



